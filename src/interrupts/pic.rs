use crate::x86::io::outb;

pub(crate) unsafe fn init_pic() {
    outb(0x20, 0x11);
    outb(0x21, 0x20); // master vectors 0x20+
    outb(0x21, 0x04);
    outb(0x21, 0x01);

    outb(0xA0, 0x11);
    outb(0xA1, 0x28);
    outb(0xA1, 0x02);
    outb(0xA1, 0x01);

    // Mask all first
    outb(0x21, 0xFF);
    outb(0xA1, 0xFF);
    // Unmask IRQ0 (timer) + IRQ1 (keyboard): bits 0 and 1 clear → 0xFC
    outb(0x21, 0xFC);
    outb(0xA1, 0xFF);
}
